# Integers
x = 1
print(x)
print(type(x))

x2 = 1000000
print(x2)
print(type(x2))

# Converting into integers
total = int(
    "100"
)  # Convert a number or string x to an integer, or return 0 if no arguments are given.
print(type(total))

age = int(input("Please enter your age:"))
print(type(age))
print(age)

# floating points numbers
exchange_rate = 1.83
print(type(exchange_rate))
print(exchange_rate)

# Float conversions
int_value = 1
string_value = "1.5"
float_value = float(int_value)
print("int_value as float value:", float_value)
print(type(float_value))
float_value2 = float(string_value)
print("string_value as float value:", float_value2)
print(type(float_value2))
